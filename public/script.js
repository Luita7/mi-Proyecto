document.addEventListener("DOMContentLoaded", () => {
    console.log("Web cargada dentro de Docker");
});

/* document.addEventListener("DOMContentLoaded", ...): 
Le decimos a JavaScript que escuche el evento "DOMContentLoaded", que ocurre cuando el contenido HTML de la página está completamente cargado.

()=>{}: Definimos lo que debe ocurrir cuando el evento sucede. En este caso, se ejecuta una función anónima.

console.log("Web cargada dentro de Docker");: Muestra un mensaje en la consola para decirnos que la página ha cargado correctamente en el navegador.*/